class RelativePosition(object):

    def __init__(self,x=0,y=0):
        self.x = x
        self.y = y

class VirtualPosition(object):

    def __init__(self):
        self.lat = 0
        self.lon = 0

class AbsolutePosition(object):

    def __init__(self):
        self.lat = 0
        self.lon = 0